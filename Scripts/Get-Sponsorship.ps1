Function Get-Sponsorship
{
    [CmdLetBinding()]Param([Parameter(Mandatory)][String]$Name)

    # // _____________________________________________________________________________________
    # // | This will contain a single slide worth of messages to be processed by Write-Theme | 
    # // ¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯

    Class SponsorshipSlide
    {
        [UInt32]        $Index
        [String]         $Name
        [String]        $Title
        [Object]  $InputObject
        SponsorshipSlide([UInt32]$Index,[String]$Name,[String[]]$InputObject)
        {
            $This.Index       = $Index
            $This.Name        = $Name
            $This.InputObject = $InputObject
        }
        SetTitle([UInt32]$Total)
        {
            $This.Title       = "{0} ({1}/{2})" -f $This.Name, ($This.Index+1), $Total
        }
        Draw()
        {
            Write-Theme -Title $This.Title -InputObject $This.InputObject
        }
        [String] ToString()
        {
            Return $This.Index
        }
    }

    # // ___________________________________________________________________________
    # // | This will contain a collection of slides to be processed by Write-Theme | 
    # // ¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯¯

    Class SponsorshipContainer
    {
        [String] $Name
        [Object] $Output
        SponsorshipContainer([String]$Name)
        {
            $This.Name   = $Name
            $This.Output = @( )
        }
        Add([String[]]$InputObject)
        {
            $This.Output += [SponsorshipSlide]::New($This.Output.Count,$This.Name,$InputObject)
            $This.Rerank()
        }
        Rerank()
        {
            If ($This.Output.Count -eq 1)
            {
                $This.Output[0].SetTitle(1)
            }
            If ($This.Output.Count -gt 1)
            {
                $C = $This.Output.Count
                ForEach ($X in 0..($C-1))
                {
                    $This.Output[$X].SetTitle($C)
                }
            }
        }
        Draw()
        {
            ForEach ($Slide in $This.Output)
            {
                $Slide.Draw()
            }
        }
    }

    [SponsorshipContainer]::New($Name)
}
