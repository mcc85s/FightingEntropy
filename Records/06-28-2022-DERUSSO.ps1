Class MP3File
{
    [String] $Name
    [String] $FullName
    [Object] $Date
    [UInt32] $Size
    [String] $SizeMB
    [String] $URL
    [UInt32] $Channels
    [UInt32] $SampleRate
    [String] $Precision
    [Object] $Duration
    [Object] $Samples
    [Object] $CDDASectors
    [String] $FileSize
    [String] $BitRate
    [String] $Encoding
    [Object] $Start
    [Object] $End
    MP3File([String]$Path,[String]$URL)
    {
        $Item             = Get-Item $Path
        $This.Name        = $Item.Name
        $This.Fullname    = $Item.Fullname
        $This.Date        = $Item.LastWriteTime
        $This.Size        = $Item.Length
        $This.SizeMb      = "{0:n3} MB" -f ($Item.Length/1MB)
        $This.Url         = "https://drive.google.com/file/d/1MkHiYnBnRl91Ck-ixcEhE5R1dX7B3Fve"
        $Sx               = soxi $Item.Fullname | ? Length -gt 0 | % Substring 17
        $This.Channels    = $Sx[1]
        $This.SampleRate  = $Sx[2]
        $This.Precision   = $Sx[3]
        $Tx               = $Sx[4] -Split " = "
        $This.Duration    = [Timespan]$Tx[0]
        $This.Samples     = $Tx[1]
        $This.CDDASectors = $Tx[2]
        $This.FileSize    = $Sx[5]
        $This.BitRate     = $Sx[6]
        $This.Encoding    = $Sx[7]
        $This.Start       = $Item.LastWriteTime-$This.Duration
        $This.End         = $Item.LastWriteTime
    }
}
