    Class ManifestFile
    {
        Hidden [UInt32]    $Index
        Hidden [UInt32]     $Mode
        [String]            $Type
        [String]            $Name
        [String]            $Hash
        [UInt32]          $Exists
        Hidden [String] $Fullname
