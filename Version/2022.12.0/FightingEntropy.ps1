        RegistryKey([Object]$Module)
        {
            $This.Name         = "Module Registry"
            $This.Path         = $Module.Root.Registry.Path
            $This.TestPath()
            If ($This.Exists)
            {
                $Object        = Get-ItemProperty $This.Path
