        {
            $Hash              = @{ }
            ForEach ($Property in $Object.PSObject.Properties | ? Name -notmatch ^PS)
            { 
                $Item          = $This.Key($Hash.Count,$Property)
                $Item.Exists   = $This.Exists
                $Hash.Add($Hash.Count,$Item)
